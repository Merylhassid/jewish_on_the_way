export function translateCity(city: string, lang: string): string {
  if (!city) return city;
  const key = city.toLowerCase().trim();
  if (lang === 'he') return CITY_HE[key] ?? city;
  if (lang === 'fr') return CITY_FR[key] ?? city;
  return city;
}

// Hook-friendly helper: translate both city and country
export function useGeoTranslation(lang: string) {
  return {
    city:    (name: string) => translateCity(name, lang),
    country: (code: string, fallback?: string) => translateCountry(code, lang) || fallback || code,
  };
}
