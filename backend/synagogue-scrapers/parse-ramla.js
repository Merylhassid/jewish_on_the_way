const seen = new Set();
const synagogues = [];

for (const line of raw.split('\n').filter(Boolean)) {
  const [name, addrRaw, denomination, gabbai, phone] = line.split('|');
  const addrClean = addrRaw.trim();
  const address = addrClean ? `${addrClean}, ${CITY}` : CITY;

  const key = `${name.trim()}|${address}`;
  if (seen.has(key)) continue;
  seen.add(key);

  const entry = { name: name.trim(), address, destinationId: DEST_ID };
  if (denomination?.trim()) entry.denomination = denomination.trim();
  if (phone?.trim())        entry.phone = phone.trim();
  if (gabbai?.trim())       entry.description = gabbai.trim();
  synagogues.push(entry);
}

